def guess_format(lines: list[str]) -> str:
    has_q = False
    has_bw = False
    has_width = False
    has_filter = False
    for line in lines:
        if line.find("AU_N-Band_EQ") != -1:
            has_width = True
        if line.find("Filter ") != -1:
            has_filter = True
        if line.find(" Q ") != -1:
            has_q = True
        if line.find(" BW ") != -1:
            has_bw = True

    if has_width:
        # generated by REW for AUNBandEQ
        return "AUNBandEQ"

    if has_filter and (has_q or has_bw):
        # more or less EQ APO format / used by autoEQ too
        return "APO"

    return "Unknown"


def parse_aunbandeq(lines: list[str]) -> tuple[STATUS, IIR]:
    iir = []
    for line in lines:
        tokens = line.split()
        len_tokens = len(tokens)
        # print(len_tokens, line)
        if len_tokens != 8 or tokens[0] == "Number" or tokens[3] == "None":
            # print('DEBUG dropping line {}'.format(line))
            continue
        iir.append(
            {
                "type": tokens[3],
                "freq": float(tokens[4]),
                "gain": float(tokens[5]),
                "width": float(tokens[6]),
            }
        )
    return True, iir


def parse_apo(lines: list[str]) -> tuple[STATUS, IIR]:
    iir = []
    for line in lines:
        tokens = line.split()
        len_tokens = len(tokens)
        # print(len_tokens, line)
        if (
            len_tokens > 0
            and tokens[0] == "Filter"
            and tokens[2] == "ON"
            and tokens[3] != "None"
        ):
            if len_tokens == 12:
                iir.append(
                    {
                        "type": tokens[3],
                        "freq": float(tokens[5]),
                        "gain": float(tokens[8]),
                        "q": float(tokens[11]),
                        "width": q2bw(float(tokens[11])),
                    }
                )
            elif len_tokens == 13:
                iir.append(
                    {
                        "type": tokens[3],
                        "freq": float(tokens[5]),
                        "gain": float(tokens[8]),
                        "q": bw2q(float(tokens[12])),
                        "width": float(tokens[12]),
                    }
                )
    return True, iir


def iir2peq(iir: IIR) -> Peq:
    peq = []
    for biquad in iir:
        biquad_type = {
            "PK": Biquad.PEAK,
            "LP": Biquad.LOWPASS,
            "HP": Biquad.HIGHPASS,
            "LS": Biquad.LOWSHELF,
            "HS": Biquad.HIGHSHELF,
            "BP": Biquad.BANDPASS,
            "LSC": Biquad.LOWSHELF,  # Low Shelf Cut - use same as Low Shelf
            "HSC": Biquad.HIGHSHELF,  # High Shelf Cut - use same as High Shelf
        }.get(str(biquad["type"]))
        if biquad_type is None:
            continue
        freq = biquad["freq"]
        gain = biquad["gain"]
        width = biquad["width"]
        q = bw2q(width)
        peq.append((1.0, Biquad(biquad_type, freq, SRATE, q, gain)))
    return peq


def lines2iir(lines: list[str]) -> tuple[STATUS, IIR]:
    option = guess_format(lines)
    if option == "AUNBandEQ":
        return parse_aunbandeq(lines)
    elif option == "APO":
        return parse_apo(lines)
    return True, []


